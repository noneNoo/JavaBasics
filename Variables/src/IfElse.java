public class IfElse {
    public static void main(String[] args) {
        int temp = 12;

        if(temp < 10) {
            System.out.println("자켓을 입으세요");
        } else if (temp > 30) {
            System.out.println("존나덥겟다");
        } else {
            System.out.println("쾌적하시겟네요");
        }
    }
}
